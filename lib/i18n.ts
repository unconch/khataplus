export const localeOptions = SUPPORTED_LOCALES.map((locale) => ({
  value: locale,
  label: dictionaries[locale].languageName,
  nativeLabel: dictionaries[locale].languageNativeName,
})) as ReadonlyArray<{ value: Locale; label: string; nativeLabel: string }>

export function isSupportedLocale(value: string | undefined | null): value is Locale {
  return SUPPORTED_LOCALES.includes((value ?? "") as Locale)
}

export function normalizeLocale(value: string | undefined | null): Locale {
  if (!value) {
    return DEFAULT_LOCALE
  }

  const normalized = value.toLowerCase()
  return isSupportedLocale(normalized) ? normalized : DEFAULT_LOCALE
}

export function getDictionary(locale: Locale): TranslationDictionary {
  return dictionaries[locale]
}
